function add(a, b){
  return a + b;
}

function subtract(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function inc(n){
  parseInt(n, 10);
  n++;
  return n;
}

function dec(n){
  parseInt(n, 10);
  n--;
  return n;
}

function makeInt(n){
  parseInt('n', 10);
  return n;
}

